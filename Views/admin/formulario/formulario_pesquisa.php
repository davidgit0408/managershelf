                                    <b>DADOS PESSOAIS</b>

                                    Entende-se por dados pessoais a informação relacionada a pessoa natural identificada ou identificável (titular dos dados), sendo considerado dado pessoal sensível o dado pessoal sobre origem racial ou étnica, convicção religiosa, opinião política, filiação a sindicato ou a organização de caráter religioso, filosófico ou político, dado referente à saúde ou à vida sexual, dado genético ou biométrico.

