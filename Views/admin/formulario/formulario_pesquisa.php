                                    <b>MEDIDAS DE SEGURANÇA ADOTADAS PELA MANAGER SHELF</b>

                                    A MANAGER SHELF assume o compromisso de proteger os dados pessoais que armazena contra danos, perda, divulgação, apropriação indevida, alteração e destruição.

