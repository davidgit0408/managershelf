            var exist_clear = false
            $(".active_tab")
                .find('input, select, textarea')
                .each(function() {
                    if ($(this).val() === "" || $(this).val() === null) {
                        if ($(this).prop('required')) {
                            $(this).css({
                                'border-color': "red"
                            });
                            exist_clear = true
                        }
                    } else {
                        $(this).css({
                            'border-color': ""
                        });
                    }
                });

            $(".active_tab").find(".checkbox-required").each((index, element) => {
                let size = $(element).find("[type=checkbox]").size();
                let sizeChecked = $(element).find("[type=checkbox]:checked").size();
                if(size > 0 && sizeChecked === 0){
                    $(element).addClass("error");
                    exist_clear = true
                } else {
                    $(element).removeClass("error");
                }
            })

            if(exist_clear) launch_toast();

            return exist_clear;
        }

        $(".next").click(function() {
            if (checkInputs()) return;
            if (validar_pesquisa()) return;
            nextPage($(this))
            window.scroll({top: 0, left: 0, behavior: 'smooth'});
        });

        $(".previous").click(function() {
            if (animating) return false;
            animating = true;
            current_fs = $(this).parent();
            previous_fs = $(this).parent().prev();
            previous_fs.show();
            current_fs.animate({
                opacity: 0
            }, {
                step: function(now, mx) {
                    scale = 0.8 + (1 - now) * 0.2;
                    left = ((1 - now) * 50) + "%";
                    opacity = 1 - now;
                    current_fs.css({
                        'left': left
                    });
                    previous_fs.css({
                        'transform': 'scale(' + scale + ')',
                        'opacity': opacity
                    });
                    current_fs.removeClass("active_tab");
                    previous_fs.addClass("active_tab");
                },
                duration: 800,
                complete: function() {
                    current_fs.hide();
                    animating = false;
                },
                easing: 'easeInOutBack'
            });
        });

        $("#cep").change(function() {
            var valor = $("#cep").val();
            var cep = valor.replace(/\D/g, '');
            if (cep != "") {
                if (String(cep).length === 8)
                    var requestOptions = {
                        method: 'GET',
                        redirect: 'follow'
                    };
                fetch(`https://viacep.com.br/ws/${cep}/json/`, requestOptions)
                    .then(response => response.text())
                    .then((result) => {
                        var endereco = JSON.parse(result);
                        if (endereco.erro) return alert('Cep Invalido.');
                        $("#estado").val(endereco.uf);
                        $("#endereco").val(endereco.logradouro);
                        $("#bairro").val(endereco.bairro);
                        $("#cidade").val(endereco.localidade);
                    })
                    .catch(error => console.log('error', error));
            }
        });

        let toggleUsaEmCasa = (element) => {
            let usa_em_casa = $("#usa_em_casa").find(".coloracao").is(":checked");
            if(!usa_em_casa) {
                $(element).hide("slow");
                $(element).removeClass("checkbox-required");
            }else{
                $(element).show("slow");
                $(element).addClass("checkbox-required");
            }
        }

        let toggle = (element) => {
            $(element).slideToggle("slow");
            $(element).toggleClass("checkbox-required");
            if (element.attr('required')) {
                element.removeAttr('required');
            } else {
                element.attr('required', true);
            }
        }

        let toggleMoraJunto = () => {
            let mora_junto = $("#qtd_pessoas_morando_junto").val();
            let element = "#quem-mora-junto";
            if(mora_junto == "1") {
                $(element).hide("slow");
                $(element).removeClass("checkbox-required");
            }else{
                $(element).show("slow");
