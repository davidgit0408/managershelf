                                    <b>CIRCUNSTÂNCIAS EM QUE EXISTE COMUNICAÇÃO DE DADOS A OUTRAS ENTIDADES</b>

                                    A MANAGER SHELF recorre a outras entidades para a prestação de determinados serviços. Eventualmente essa prestação de serviços poderá implicar acesso, por estas entidades, a dados pessoais dos seus clientes, fornecedores, parceiros e utilizadores do site.

                                    Assim, qualquer entidade subcontratada do A MANAGER SHELF tratará os dados pessoais dos titulares dos dados na estrita obrigação de seguir a LGPD.

                                    Em determinadas situações, os dados pessoais dos titulares dos dados poderão ainda ser transmitidos a entidades terceiras, quando tais comunicações de dados sejam necessárias ou adequadas (i) à luz da lei aplicável, (ii) no cumprimento de obrigações legais/ordens judiciais, (iii) por determinação da Autoridade Nacional de Proteção de Dados (ANPD) ou (iv) para responder a solicitações de autoridades públicas ou governamentais.

                                    Em qualquer das situações acima mencionadas, A MANAGER SHELF compromete-se a tomar todas as medidas razoáveis para garantir a proteção efetiva dos dados pessoais que trata.

