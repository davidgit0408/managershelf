                                    <b>DIREITOS DOS TITULARES DOS DADOS</b>

                                    Nos termos do art. 9º da LGPD, o titular dos dados tem direito ao acesso facilitado às informações sobre o tratamento de seus dados, que deverão ser disponibilizados de forma clara, adequada e ostensiva acerca de, entre outras, características previstas em regulamentação para o atendimento do princípio do livre acesso: I – finalidade específica do tratamento; ii – forma e duração do tratamento, observados os segredos comercial e industrial; iii – identificação do controlador; iv – informações do contato do controlador; v- informações acerca do uso compartilhado de dados pelo controlador e finalidade; vi – responsabilidades dos agentes que realizarão o tratamento; e vii – direitos do titular, com menção explícita aos direitos contidos no art. 18 da LGPD.

                                    O titular dos dados poderá exercer os referidos direitos mediante pedido escrito dirigido ao responsável pela Proteção de Dados da MANAGER SHELF para o endereço de email contato@managershelf.com.br.

