                                    <b>CATEGORIAS DE DADOS PESSOAIS QUE TRATAMOS E FINALIDADES DO TRATAMENTO</b>

                                    Os dados pessoais dos clientes são geralmente utilizados no âmbito da prestação e gestão dos serviços de tecnologia para pesquisa de mercado. O tratamento dos dados dos clientes encontrar-se-á, na maioria dos casos, legitimado pela relação contratual estabelecida entre o MANAGER SHELF e o titular dos dados, e, em determinadas situações, para cumprimento de obrigações legais impostas ao escritório.

                                    Os dados pessoais dos fornecedores (pessoas singulares), i.e., pessoas singulares que representam ou colaborem com os fornecedores e prestadores de serviços a MANAGER SHELF, a que o escritório tenha acesso no âmbito da prestação de serviços, são tratados para efeito de gestão da relação contratual. Este tratamento encontra-se legitimado pela execução do contrato estabelecido entre a MANAGER SHELF e o fornecedor, e, em determinadas situações, para cumprimento de obrigações legais impostas ao escritório.

                                    No contexto de relação com parceiros, i.e., pessoas singulares e seus colaboradores com quem a MANAGER SHELF estabeleça uma relação de parceria, e em relação aos dados necessários para a gestão da parceria pela MANAGER SHELF, o tratamento encontra-se legitimado pela execução do contrato estabelecido entre a MANAGER SHELF e o parceiro, e, em determinadas situações, para cumprimento de obrigações legais impostas ao escritório.

                                    Os informativos relacionados a temas de tecnologia poderão ser enviados por meio do endereço de email cadastrado em nosso banco de dados ou outras formas de chamada automática, onde é garantido que a informação prestada se encontra ao abrigo do endereço de email cadastrado pelo cliente e que por isso exclui a necessidade de obtenção de consentimento. Os Clientes deverão ter em conta que não partilhamos os seus dados pessoais com terceiros. Caso a pessoa não queira receber mais comunicações do gênero, basta solicitar por email ou acessar no informativo o link específico para descadastrar o seu contato.

                                    <b>PERÍODO DE CONSERVAÇÃO DOS DADOS PESSOAIS</b>

                                    Os dados pessoais recolhidos serão conservados pela MANAGER SHELF pelo período em que vigore a sua relação com o cliente, fornecedor ou parceiro, podendo ser conservados por período superior, estabelecido na lei ou para defesa de direito/interesse em processo judicial, ou, ainda, para envio de informações jurídicas, nos termos acima indicados.

